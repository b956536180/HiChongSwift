import UIKit

extension UIColor {
    /**
    TabBar的背景色
    
    :returns: 颜色值UIColor
    */
    class func LCYTabBarBarTintColor() -> UIColor {
        return UIColor(white: 80.0/255.0, alpha: 1.0)
    }
    
    /**
    嗨宠主题浅蓝色
    
    :returns: 颜色值UIColor
    */
    class func LCYThemeColor() -> UIColor {
        return UIColor(red: 88.0/255.0, green: 158.0/255.0, blue: 188.0/255.0, alpha: 1.0)
    }
}
